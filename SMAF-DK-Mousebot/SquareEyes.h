class SquareEyes {
public:
  SquareEyes(Adafruit_SSD1306 &display);

  // Initialize the library.
  void begin();

  // Update the animation (call in loop).
  void update();

  // Set the blink interval range (in milliseconds).
  void setBlinkIntervalRange(unsigned long minInterval, unsigned long maxInterval);

  // Set the blink duration (in milliseconds).
  void setBlinkDuration(unsigned long duration);

  void flipVertical(bool enable);

  // Expressions.
  void neutral();
  void lookLeft();
  void lookRight();
  void wonder();
  void annoyed();
  void dizzy();
  void charging();
  void chargingComplete();
  void cleanMode();

  // Idle mode (randomly cycle through expressions).
  void idle();

private:
  Adafruit_SSD1306 &display;

  const uint8_t *currentBitmapOpen;    // Pointer to current open eyes bitmap.
  const uint8_t *currentBitmapBlink;   // Pointer to current blink eyes bitmap.
  static const int bitmapWidth = 128;  // Fixed width of the bitmaps.
  static const int bitmapHeight = 64;  // Fixed height of the bitmaps.

  // Timing variables.
  unsigned long lastBlinkTime;       // Last blink start time.
  unsigned long blinkInterval;       // Time between blinks.
  unsigned long blinkDuration;       // Duration of a single blink.
  unsigned long minBlinkInterval;    // Minimum blink interval.
  unsigned long maxBlinkInterval;    // Maximum blink interval.
  unsigned long lastIdleChange;      // Last time the expression was changed in idle mode.
  unsigned long idleChangeInterval;  // Interval between random expression changes.
  bool isStaticBitmap;               // Static bitmap state flag.
  bool isBlinking;                   // Blink state flag.
  bool isIdleMode;                   // Tracks whether the library is in idle mode.
  int lastExpression = -1;           // Initialize to an invalid expression index.

  // Private methods.
  void drawBitmap(const uint8_t *bitmap);  // Draw a single bitmap on the display.
  void setRandomExpression();              // Set a random expression.
  void calculateNextBlinkInterval();       // Calculate the next random blink interval.
};

